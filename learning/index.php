							</h2>
							<p class='professor'>
								PROFESSOR PAUL MIHAILIDIS
							</p>
							<p class='course'>
								Global Media Literacy, a summer course that convenes in Salzburg, Austria at the <a href='http://salzburg.umd.edu/'>Academy on Media & Global Change</a>  explores how digital media technologies are shaping civic engagement, activism, and innovation in a global media age. Students work in a truly dynamic global cohort of fellow university students from 15 partner institutions, to build digital stories, business plans, and content that helps provide innovative insights into the role of media, technology, and activism in changing societies across borders, across cultures, and across divides. Specifically, the course examines how distinctive global media shape views of politics, culture and society within nations, across regions and internationally. GML will help students develop an informed and critical understanding of how media shape and influence society and communication in the global public sphere. The course will develop a critical understanding of ideas around participatory technologies, collaborative media, social networks, mobile platforms and digital culture. This past summer, the partner for our course was the United Nations Development Programme (UNDP), Knowledge, Innovation and Capacity Group.
