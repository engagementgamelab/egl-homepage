							<div class='span9 bookDescription'>
								<p>
									Like real-world programs and projects, games involve decisions with consequences. The development and humanitarian sectors confront challenges and opportunities in a changing context dominated by complexities, volatilities and uncertainties. An increasing body of evidence shows that participatory games can help people and organizations &ldquo;inhabit&rdquo; the complexity of risky decisions, allowing us to explore, then test a range of plausible futures. Albert Einstein once said that &ldquo;games are the most elevated form of investigation&rdquo;. When adequately designed, games can capture essential aspects of reality: through simple rules, games create emergent complexity that meaningfully engages us in learning and dialogue about options and outcomes - in a way that is both serious and fun. They can also help to facilitate complex and difficult professional processes and provide a mechanism for players/participants to be more creative and provide better input.
								</p>
