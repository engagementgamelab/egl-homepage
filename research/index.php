							<h4> Networked Citizenship </h4>
							<p>
								A multi-year research project to explore transformations in the definition and qualities of citizenship in the context of new digital tools and practices. It explores concepts of play, connectivity, and attention in civic life and asks how these qualities augment trust and efficacy. The research is based on empirical data gathered from the lab’s affiliated games and tools.
							</p>
						</div>
