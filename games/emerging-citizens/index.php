							<p>
								<span>The Curricular Arc:</span>
								<img class='inlineImageRightBig' src='../../img/projects/emerging-citizens/curriculummap.png'>
								Students choose an issue or problem with global and local resonance. Then, they move through a series of four modules, each beginning with a lesson and culminating in a round of game play. Lessons onboard players with the hands-on skills and content knowledge they will need to complete game round challenges. The modules are cumulative, taking students on a journey from exploration of personal stories to critical analysis of relevant media to pitching actionable solutions or approaches to the issue at hand. In culmination, students implement one of these solutions in their community.
								<br><br><br><br>
							</p>
							<p>
								<span>Playing the Game:</span>
								<img class='inlineImageLeftBig' src='../../img/projects/emerging-citizens/gameplayflow.png'>
								In teams, students progress through four rounds of play, each addressing a distinct aspect of media literacy. Teams create challenges for each other to complete and then select their favorites. Teams complete their chosen challenges and evaluate each other’s responses.
							</p>
							<p>
								Teams play a short mini-game to determine which one gets to choose their challenge first. Then, for consistency in the evaluation stage, the other team completes the same challenges. After choosing their challenge, teams encounter another element of chance by drawing a random modality card (storyboarding, persuasive letter, media remix, etc.) that guides their response. When teams evaluate each other’s responses, they use rubrics tailored to the goals of that particular module.
							</p>
