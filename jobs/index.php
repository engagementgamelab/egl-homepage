						<h4>Why a Community IRB</h4>
						<p>
							When University faculty conduct research involving human subjects, they are required to work through an institutional review board (IRB) that reviews research protocols to assure compliance with federal ethics standards. IRB protocols were initially established to protect individual research subjects from physical and emotional harm and ultimately protect research institutions from liability. But they often fall short in ensuring that research is mutually beneficial for the researcher and the research subject. In the health fields, there have been some innovative efforts to create <a href="https://ccph.memberclicks.net/assets/Documents/FocusAreas/shaping_the_future_of_cenr.pdf">Community IRBs</a>, which are bottom-up ethics standards, written by and enforced by the populations most likely to be studied. As civic technology continues to be tested in cities throughout the world, and human subjects are routinely tapped to “test” tools and approaches, there is need to develop city-level Community IRBs specifically geared towards civic tech research.
						</p>
