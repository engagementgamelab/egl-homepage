			<h4 id='cmp-subtitle'>An Initiative of the Engagement Lab at Emerson College and MIT Press</h4>
			<p>The Civic Media Project, an initiative to define and discuss civic media through global collaboration, will be released in March 2015. The site features over 100 case studies from experts in their respective fields, leaders of organizations, policy makers, and academics. The case studies cover everything from civic crowdfunding campaigns, hashtag activism, civic collaboration, play, and citizenship from dozens of countries throughout the globe.  Features on the site will allow for readers' comments, discussion, peer review and new case study submissions.</p>
			<p>As a living document, the Civic Media Project is a platform for sharing, learning, and discussing civic media for a diverse audience including activists, academics, and practitioners.  Civic Media: Technology, Design and Practice (MIT Press, 2016) is the print book companion to the Civic Media Project. It seeks to identify the emerging field of Civic Media by bringing together thought leaders from a diversity of disciplines to shape theory, identify problems and articulate opportunities.  The book includes 19 essays and 25 case studies representing a range of domains including government, education, social movements, and political campaigns. </p>
